package Week02;
public class Student {

	
		private int stuId;
		private String name;
		private double fee;
		
		public int getStuId() {
			return stuId;
		}

		public void setStuId(int stuId) {
			this.stuId = stuId;
		}

		public String getName() {
			return name;
		}

		public void setName(String name) {
			this.name = name;
		}

		public double getFee() {
			return fee;
		}

		public void setFee(double fee) {
			this.fee = fee;
		}
		
		
		
	
}
